<?php
function tambah($a, $b)
{
    return $a + $b;
}

function kurang($a, $b)
{
    return $a - $b;
}
function kali($a, $b)
{
    return $a * $b;
}
function bagi($a, $b)
{
    return $a / $b;
}


echo tambah(20, 10) . "<br>"; //tampil 3

echo kurang(20, 10);

echo "<br>";
echo kali(20, 10);

echo "<br>";
echo bagi(20, 10);

echo "<br>";
